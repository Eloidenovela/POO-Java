/*
 * Escreva um programa Java para encontrar valores duplicados em uma matriz de valores de
 * string.
 */

public class Exer12 {
    public static void main(String[] args) {
        String[][] matriz ={
            {"o", "mm", "mm"}, 
            {"66", "yy", "oo", "ooo", "ooop"}
        };

        for (int i = 0 ; i < matriz[1].length ; i++) {
            System.out.println(matriz[1][i] + " ");
        }
    }
}
